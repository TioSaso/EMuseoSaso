    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Casting
        Nacionalidad p = (Nacionalidad) nacionalidad.getSelectedItem();
        if (contam > 0) {
            String fecha3 = new SimpleDateFormat("yyyy/MM/dd").format(fNacimiento.getDate());
            String fecha4 = new SimpleDateFormat("yyyy/MM/dd").format(fDefuncion.getDate());
            contam = 0;
            crud.modificarAutor(cod, nombreAutor.getText(), fecha3, fecha4, p.getCodigoNacionalidad());
            JOptionPane.showMessageDialog(this, "Se ha Modificado.");
        } else {
            String fecha1 = new SimpleDateFormat("yyyy/MM/dd").format(fNacimiento.getDate());
            String fecha2 = new SimpleDateFormat("yyyy/MM/dd").format(fDefuncion.getDate());
            //Se crea
            crud.insertarAutor(nombreAutor.getText(), fecha1, fecha2, p.getCodigoNacionalidad());
            //Se avisa
            JOptionPane.showMessageDialog(this, "Se ha Grabado.");
        }
        //Limpiar las cajas
        nombreAutor.setText("");
        fNacimiento.setDateFormatString("");
        fDefuncion.setDateFormatString("");
        //Cargar los datos en la tabla
        llenartabla();

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (fila > -1) {
            nombreAutor.setText(String.valueOf(jTable1.getValueAt(fila, 1)));
            cod = Integer.parseInt(String.valueOf(jTable1.getValueAt(fila, 0)));
            fNacimiento.setDateFormatString(String.valueOf(jTable1.getValueAt(fila, 2)));
            fDefuncion.setDateFormatString(String.valueOf(jTable1.getValueAt(fila, 3)));
            fila = -1;
            contam++;

        } else {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar un Registro", "Advertencia", JOptionPane.OK_OPTION + JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (fila > -1) {
            int res = JOptionPane.showConfirmDialog(this, "Â¿Desea eliminar al Autor " + String.valueOf(jTable1.getValueAt(fila, 1))
                    + " ?", "Advertencia", JOptionPane.OK_CANCEL_OPTION);
            if (res == 0) {
                crud.eliminarAutor(Integer.parseInt(String.valueOf(jTable1.getValueAt(fila, 0))));
                llenartabla();
            }
            fila = -1;
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Registro", "Advertencia", JOptionPane.OK_OPTION + JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

