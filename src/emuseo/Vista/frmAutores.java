    //Metodo para llenar la tabla
    private void llenartabla() {
        //Indicar que empezamos por la columna 0
        tabla.setColumnCount(0);
        //Agregar columnas
        tabla.addColumn("Id Autor");
        tabla.addColumn("Nombre Autor");
        tabla.addColumn("Fecha Nacimiento");
        tabla.addColumn("Fecha Defuncion");
        tabla.addColumn("Nacionalidad");
        //Recibir el ArrayList
        ArrayList<Autores> lista = crud.mostrarAutores("");
        //Determinar la cantidad de filas de la tabla
        tabla.setNumRows(lista.size());
        //Llenar la tabla
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getCodigoAutor(), i, 0);
            tabla.setValueAt(lista.get(i).getNombreAutor(), i, 1);
            tabla.setValueAt(lista.get(i).getFechaNacimientoAutor(), i, 2);
            tabla.setValueAt(lista.get(i).getFechaDefuncionAutor(), i, 3);
            tabla.setValueAt(lista.get(i).getNombreNacionalidad(), i, 4);
        }
        //Enviar los datos a la vista(tabla)
        jTable1.setModel(tabla);
    }

