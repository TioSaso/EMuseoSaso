    //Metodo para mostrar lista de Paises
    private void llenarpaises() {
        ArrayList<Pais> lista = crud2.mostrarPais("");
        for (Pais p : lista) {
            p = new Pais(p.getNombrePais());
            datos.addElement(p);
        }
        pais.setModel(datos);
    }

    //Metodo para llenar la tabla
    private void llenartabla() {
        //Indicar que empezamos por la columna 0
        tabla.setColumnCount(0);
        //Agregar columnas
        tabla.addColumn("Id Nacionalidad");
        tabla.addColumn("Nacionalidad");
        tabla.addColumn("Pais");
        //Recibir el ArrayList
        ArrayList<Nacionalidad> lista = crud.mostrarNacionalidad("");
        //Determinar la cantidad de filas de la tabla
        tabla.setNumRows(lista.size());
        //Llenar la tabla
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getCodigoNacionalidad(), i, 0);
            tabla.setValueAt(lista.get(i).getNombreNacionalidad(), i, 1);
            tabla.setValueAt(lista.get(i).getNombrePais(), i, 2);
        }
        //Enviar los datos a la vista(tabla)
        jTable1.setModel(tabla);
    }

