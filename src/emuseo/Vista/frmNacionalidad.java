    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Casting
        Pais p = (Pais) pais.getSelectedItem();
        if (contam > 0) {
            contam = 0;
            crud.modificarNacionalidad(cod, nombreNacionalidad.getText(), p.getCodigoPais());
            JOptionPane.showMessageDialog(this, "Se ha Modificado.");
            //Limpiar las cajas
            nombreNacionalidad.setText("");
            //Cargar los datos en la tabla
            llenartabla();
        } else {
            //Se crea
            crud.insertarNacionalidad(nombreNacionalidad.getText(), p.getCodigoPais());
            //Se avisa
            JOptionPane.showMessageDialog(this, "Se ha Grabado.");
            //Limpiar las cajas
            nombreNacionalidad.setText("");
            //Cargar los datos en la tabla
            llenartabla();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (fila > -1) {
            int res = JOptionPane.showConfirmDialog(this, "Â¿Desea eliminar la Nacionalidad " + String.valueOf(jTable1.getValueAt(fila, 1))
                    + " ?", "Advertencia", JOptionPane.OK_CANCEL_OPTION);
            if (res == 0) {
                crud.eliminarNacionalidad(Integer.parseInt(String.valueOf(jTable1.getValueAt(fila, 0))));
                llenartabla();
            }
            fila = -1;
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Registro", "Advertencia", JOptionPane.OK_OPTION + JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

