    private void cerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cerrarActionPerformed
        //llamar formulario Login
        frmLoginAdmin frm = new frmLoginAdmin();
        visor.add(frm);
        centrar(frm);
        cerrar.setEnabled(false);
        iniciar.setEnabled(true);
        try {
            JInternalFrame frames[] = visor.getAllFrames();
            DesktopManager dm = visor.getDesktopManager();
            for (int i = 0; i < frames.length; i++) {
                dm.closeFrame(frames[i]);
                frames[i].setClosed(false);
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_cerrarActionPerformed

    private void catalogoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_catalogoMouseClicked
        if (privilegio >= 1) {
            if (jMenuBar1.isEnabled()) {
                frmCatalogo frm = new frmCatalogo();
                visor.add(frm);
                frm.show();
                try {
                    frm.setMaximum(true);
                } catch (Exception x) {
                    System.out.println("Error: " + x);
                }
            }
        }
    }//GEN-LAST:event_catalogoMouseClicked

