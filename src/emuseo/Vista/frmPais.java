    private void llenartabla() {

        //Indicar que empezamos por la columna 0
        tabla.setColumnCount(0);
        //Agregar columnas
        tabla.addColumn("Id Pais");
        tabla.addColumn("Nombre Pais");
        //Recibir el ArrayList
        ArrayList<Pais> lista = crud.mostrarPais("");
        //Determinar la cantidad de filas de la tabla
        tabla.setNumRows(lista.size());
        //Llenar la tabla
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getCodigoPais(), i, 0);
            tabla.setValueAt(lista.get(i).getNombrePais(), i, 1);
        }
        //Enviar los datos a la vista(tabla)
        jTable1.setModel(tabla);
    }

