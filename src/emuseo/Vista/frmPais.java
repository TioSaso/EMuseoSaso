    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (contam > 0) {
            contam = 0;
            crud.modificarPais(cod, nombrePais.getText());
            JOptionPane.showMessageDialog(this, "Se ha Modificado.");
        } else {
            //Se crea
            crud.insertarPais(nombrePais.getText());
            //Se avisa
            JOptionPane.showMessageDialog(this, "Se ha Grabado.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (fila > -1) {
            nombrePais.setText(String.valueOf(jTable1.getValueAt(fila, 1)));
            cod = Integer.parseInt(String.valueOf(jTable1.getValueAt(fila, 0)));
            fila = -1;
            contam++;
        } else {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar un Registro", "Advertencia", JOptionPane.OK_OPTION + JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (fila > -1) {
            int res = JOptionPane.showConfirmDialog(this, "Â¿Desea eliminar la Galeria " + String.valueOf(jTable1.getValueAt(fila, 1))
                    + " ?", "Advertencia", JOptionPane.OK_CANCEL_OPTION);
            if (res == 0) {

                crud.eliminarPais(Integer.parseInt(String.valueOf(jTable1.getValueAt(fila, 0))));
                llenartabla();
            }
            fila = -1;
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Registro", "Advertencia", JOptionPane.OK_OPTION + JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

